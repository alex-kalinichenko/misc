       <h1>Недорогой фотограф на свадьбу в Москве</h1>
       <br />
       <p><i>Я предлагаю обсудить тему недорогих фотографов на свадьбу не только в городе Москва, но и вообще в принципе. Разберёмся, что именно мы понимаем под словом недорого и сколько это на самом деле в рублях. А также проанализируем, что хочет клиент, пытающийся найти фотографа на свадьбу недорого.</i></p>
       <br />
       <h2>Недорого. Сколько это?</h2>
       <p>Все оперируют разными понятиями о слове недорого. Для одного, 500 рублей - это дорого. А для другого и 10 тысяч - нормальная цена за час. Но так как рынок в Москве давно переполнен предложениями дорогих фотографов, нужно не полениться и пролистать хотя бы 10 страниц в поисковике, чтобы сложить свое мнение. В среднем недорогим фотографом считается тот, у кого стоимость услуг не превышаем 1000 рублей в час (при условии фотосъёмки не в фотостудии. Для студийной съёмки умножайте примерно на 1,5-2). Средний уровень свадебных фотографов в Москве запрашивает от тысячи до двух. Ну а те кто берёт выше 2000 считаются дорогими. После определение с терминами "дорого" и "недорого", давайте обсудим другие параметры, которые при выборе услуг недорого фотографа на свадьбу в Москве важны не меньше стоимости.</p>
       <img src="inexpensive-photographer/inexpensive-photographer-1.jpg" width="540" height="405" alt="Недорогой свадебый фотограф в Москве" />
       <br /><br />
       <h2>Соотношение цена/качество недорого фотографа</h2>
       <p>Этот параметр по праву является определяющим. Он позволяет разграничить недорогих хороших фотографов от недорогих плохих. Думаю, никому не нужен любитель, даже если он просит 100 рублей за 12 часовую съёмку. А вот если бы, например, такой культовый фотограф как <a href="george_hurrell.php" title="Джордж Харрелл" target="_blank">Джордж Харрелл</a> сейчас был бы жив, то установи он стоимость например в 5000 в час, ему пришлось бы не есть и не спать, а только фотографировать и всё равно он не смог бы сфотографировать всех желающих.</p>
       <p>Если бы можно было объективно оценить качество каждого фотографа по какой-либо шкале, то потом простым делением цены на качество мы бы вывели по каждому недорогому фотографу таблицу. И могли бы объективно выбирать услуги самого хорошего и одновременно недорогого как на свадьбу, так и на обычную фотосессию.</p>
       <p>Но увы не всё так легко. Оценивать фотографов приходится на глаз, приблизительно, по различным критериям (подробнее читайте в статье <a href="photographer-wedding-moscow.php" title="Как выбрать фотографа на свадьбу в Москве" target="_blank">про свадебных фотографов</a>).</p>
       <img src="inexpensive-photographer/inexpensive-photographer-2.jpg" width="540" height="360" alt="Недорогой фотограф на свадьбу в Москве" />
       <br /><br />
       <h2>Почему именно недорогой фотограф?</h2>
       <p>Люди, которые выбирают наилучшего фотографа из недорогих достойны всяческого уважения. Про них можно сказать, что они умеют считать деньги. Они не хотят переплачивать за бренд и хотят хорошую услугу по приемлемой цене.  В наше время это качество присутствует у очень немногих. Большинство привыкло считать - дорого, значит хорошо. И совсем не пытаются разобраться <a href="expensive-photographer.php" title="Дорогой фотограф" target="_blank">за что они платят деньги</a> и чем отличаются разные свадебные фотографы. В фотографии, как и в любой профессии есть слабые и сильные. И совсем необязательно стоимость отражает их профессионализм. Поэтому я очень советую внимательно изучать каждую кандидатуру.</p>
       <p>Автор этой статьи в свое время тоже воспользовался услугами весьма недорогого московского свадебного фотографа и остался весьма доволен результатом. Просто на поиски такого фотографа надо потратить немного больше времени. Вот и весь секрет успеха. Удачи вам!</p>
