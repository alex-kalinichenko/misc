       <h1>Фотограф на свадьбу в Москве</h1>
       <br />
       <p><i>В этой статье я расскажу как выбирать свадебного фотографа (не только в Москве) и на что обращать внимание. Перечислю критерии, ориентируясь на которые вы сможете выбрать фотографа, который вас полностью устроит. Раскрою тайну, почему есть хорошие фотографы стоящие дёшево и есть плохие стоящие дорого. А также расскажу, кому уходят большие деньги, которые зарабатывают самые раскрученные фотографы и почему им остаётся совсем небольшая часть заработаного.</i></p>
       <br />
       <h2>Фотографы - дорогие и дешевые, плохие и хорошие</h2>
       <br />
       <p>Количество свадебных фотографов в Москве поражает воображение. Каждый, купивший зеркальную камеру (даже полупрофессиональную), начинает считать себя если не  профессиональным фотографом, создающего шедевры, то как минимум неплохим фотографом, который может вполне прилично фотографировать. Даже если он нигде не учился искусству фотографии. Всё как в анекдоте:</p>
       <p><i>Встречаются два фотографа. Один другому говорит:  - Я теперь хирург. - Почему?  - Скальпель купил.</i></p>
       <p>Сейчас просторы интернета завалены объявлениями  "профессиональных" фотографов, снимающих свадьбы и корпоративы, репортажки и студийные фотосессии, love story и, даже готовые создавать портфолио для профессиональных моделей.</p>
       <p>Не нужно верить каждому на слово. При выборе свадебного фотографа не только в Москве, а и в любой точке мира, нужно подойти к этому со всей обстоятельностью. Вы же хотели бы жениться (или выйти замуж) единственный раз и на всю жизнь? Тогда и шанс сделать качественные свадебные фотографии будет только один. И всё будет зависеть только от мастерства приглашённого свадебного фотографа.</p>
       <p>Проще, конечно, найти фотографа через интернет. Можно сразу посмотреть и оценить его работы. Обычно для поиска фотографов пользуются Яндексом или Гуглом. Чаще всего пользователи вводят в запрос ключевые слова "<strong>фотограф на свадьбу москва</strong>" или "<strong>фотограф свадьба москва</strong>".  Яндекс по первому запросу выдаёт около 10 миллионов ответов! Есть отчего растеряться! Я очень рекомендую пролистать несколько страниц яндекса и просмотреть несколько десятков сайтов свадебных фотографов в Москве. </p>
       <p>Статистика говорит, что 90% пользователей переходят только по первым трём ссылкам, появившимся в поисковой выдаче. Остальным достаётся жалкие 10% просмотров. На вторые, третьи и так далее страницы не переходит почти никто. Тогда получается, что вся задача фотографа, как хозяина ресурса состоит даже не совсем в качественной и креативной фотографии. Для того, чтобы у фотографа было много клиентов, нужно, чтобы его сайт был в тройке, или хотя бы в топ-10. Вот здесь то и начинается самое интересное!</p>
       <p>Для того, чтобы ваш сайт был в ТОПе, нужно использовать специальные методы, по продвижению  или, говоря более правильно, методы поисковой оптимизации. Существуют отдельная профессия и большое количество фирм, специализирующихся на этом бизнесе. И конечно же, стоит это недёшево. Например, продвижение сайта фотографа, снимающего свадьбы в Москве, по запросу "<strong>фотограф на свадьбу москва</strong>" или "<strong>фотограф свадьба москва</strong>", что одно и тоже, стоит от 100 до 150 тысяч рублей ежемесячно!!! Я думаю теперь вам понятно, куда уходят гонорары фотографов из первых строчек в Яндексе и Гугле.</p>
       <p>Именно поэтому у фотографов с первой страницы поисковиков самые высокие цены. Им приходится завышать стоимость просто для того, чтобы выжить, чтобы поток клиентов не прекращался, и для того, чтобы оплачивать работу оптимизаторов. Значит  дорогой фотограф совершенно не обязательно будет хорошим и профессиональным. А раз так, то при выборе свадебного фотографа очень важно отбросить критерии стоимости, и критерий того, находится ли ссылка на его ресурс в верхних позициях поисковых систем. А по каким же критериям тогда его выбирать, спросите вы? Об этом и пойдёт речь дальше.</p>
       <br />
       <h2>Как будем выбирать?</h2>
       <br />
       <p>Как я уже сказал, обязательно просмотрите не одну десятку свадебных фотографов. Чем больше, тем лучше. Покажите понравившиеся фотографии родственникам и друзьям, выслушайте их мнения, узнайте что им нравиться, а что нет и решите для себя какие параметры важны для вас.</p>
       <p>Также обращайте внимание не на самые лучшие фотографии конкретного фотографа, а на самые худшие. Внимательно изучите те фотосессии, которые вам не кажутся красивыми и проанализируйте, почему вы не хотели бы таких фотографий. Это важно потому что если у свадебного фотографа есть такие фотосессии, которые вам нравятся и такие, которые не нравятся, то нет никакой гарантии, что если вы выберете этого фотографа, вы не получите фотографии именно в том стиле, который вас совершенно не устраивает.</p>
       <p>Когда вы будете изучать то, как работает фотограф, обращайте внимание на следующие моменты:</p>
       <ul id="list">
       	<li><b>Нравятся ли вам позы</b>, в которых находятся новобрачные. Большинство поз постановочные. Естественно, что сам фотограф на свадебной прогулке подсказывает, как становится и в какую сторону смотреть молодожёнам. Вы же не хотите принимать такие позы, которые вам кажутся удачными, а на самом деле совсем таковыми не являются? Вы можете принять такие позы по совету свадебного фотографа, а потом будете расстраиваться, рассматривая не самые удачные кадры.
