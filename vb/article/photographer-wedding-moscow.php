       	</li>
       	<br />
       	<li>Устраивают ли вас <b>выражения лиц</b>? Талант заключается также в том, чтобы подловить такие моменты, когда люди выглядят наиболее естественно и расслабленно. Такими мы их знаем и любим. Если же фотограф будет фотографировать не задумываясь, то результат может очень сильно разочаровать. Более того, можно сделать очень колоритные, неожиданные и забавные фотографии, которые между тем никому не понравятся. Гости будут обижаться за то, что их поймали в не самые удачные моменты. Большинство любит, когда выглядит в самом выигрышном свете.
