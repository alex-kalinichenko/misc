       	<li><b>Очень важно на каком фоне вас будут фотографировать на свадьбе</b>. Молодожёны обычно сами выбирают маршрут свадебной прогулки и достопримечательности на фоне которых они будут фотографироваться. В Москве огромный выбор красивых мест и парков. Но необходимо ещё на стадии выбора фотографа решить, устраивает ли вас в принципе то, что находится на заднем плане? Симпатичны ли вам ракурсы и композиции того, на фоне чего позируют новобрачные? Есть фотографы, которые очень несерьёзно относятся к тому, что находится на заднем плане фотографии. Если их что-то не устраивает, они запросто это могут "подправить" при помощи фотошопа. Есть и таки умельцы, которые из свадебной фотосессии делают свадебный фотоколлаж! Они просто "врезают" жениха и невесту в какой-либо пейзаж или интерьер. Даже если они там никогда не были! Может быть кому то это и покажется красивым, но только не настоящим профессионалам. Настоящий фотограф создаёт шедевральные снимки, а не занимается изготовление коллажей в фотошопе. Тем более у свадебных фотографов в Москве нет недостатка в живописных местах.
