       	</li>
       	<br />
       	<li><b>Ракурсы</b> которые выбирает фотораф должны вас тоже устравать. Я думаю, вы хотите, чтобы рассматривая ваш семейный альбом, друзья и родственники замирали от восхищения, а не покатывались со смеху.
       	<br />
