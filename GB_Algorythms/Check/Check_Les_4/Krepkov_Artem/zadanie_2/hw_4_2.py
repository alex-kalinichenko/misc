def hw_4_2_v2(y):
    # y = abs(int(input('Введите натуральное число: ')))  # не забудь убрать y в функции
    classic = []
    for i in range(2, y):
        result = True
        for j in range(2, i):
            if i % j == 0:
                result = False
        if result:
            classic.append(i)
    return classic


