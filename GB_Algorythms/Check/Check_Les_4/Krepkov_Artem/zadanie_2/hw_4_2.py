def hw_4_2_v1(x):
    # x = abs(int(input('Введите натуральное число: '))) # не забудь убрать x в функции
    era = list(range(x + 1))
    era[1] = 0
    for i in era:
        if i > 1:
            for j in range(i + i, len(era), i):
                era[j] = 0
    return era


